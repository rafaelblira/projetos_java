public class WhileExemplo1 {
    public static void main(String[] args) {
        int cont = 0;
        while (cont < 10){
            System.out.println("Contagem progessiva " + cont++);
        }
        //cada linha de comando da estrutura while em Java é limitada por ponto-e-virgula;
        //o agrupamento da estrutura while em Java é limitado por chaves {...}

    }
}